export function renderSummary(data) {
    const statsElement = document.getElementById("stats");
    const topKeyWordElement = document.getElementById("top_keyword");
    const articlesCountElement = document.getElementById("articles_count");
    const mostCommonCategoryElement = document.getElementById("most_common_category");
    const avgHeadlineLengthElement = document.getElementById("avg_headline_length");

    if (topKeyWordElement) {
        topKeyWordElement.textContent = Object.keys(data.keywords || {})[0] ?? "–";
    }
    
    if (articlesCountElement) {
        articlesCountElement.textContent = data.articles?.length ?? 0;
    }
    
    if (mostCommonCategoryElement) {
        mostCommonCategoryElement.textContent = data.metrics?.most_common_category ?? "–";
    }
    
    if (avgHeadlineLengthElement) {
        avgHeadlineLengthElement.textContent = (data.metrics?.avg_headline_length ?? 0) + " words";
    }
    
    if (statsElement && data.meta) {
        statsElement.innerHTML = `Generated by <a href="https://www.linkedin.com/in/lorenzokniss/" target="_blank" rel="noopener noreferrer">Lorenzo Kniss</a> at: ${data.meta.generated_at} | Load: ${data.meta.execution_time} | Memory: ${data.meta.memory_usage}`;
    }
}